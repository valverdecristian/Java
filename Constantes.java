public class Constantes {
    public static void main(String[] args){

        // las constantes se definen usando la palabra clave "final"
        final String empresa = "limited";
        System.out.println(empresa);
    }
}


/**
 * CONSTANTES
 * 1) Constantes a nivel clase: se declaran como "static final"
 *      -static: la constante pertenece a la clase, no a una instancia especifica.
 *      -final: el valor no cambia despues de su inicializacion.
 * 
 * 2) Constantes a nivel de instancia: se declaran como "final"
 * 
 */