package FlujoDeControl;

public class OperadoresDeComparacion {
    public static void main(String[] args) {
        int a = 15;
        int b = 10;

        // compara el valor de ambas variables
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
    }
}
